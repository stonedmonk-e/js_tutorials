
month = 7

switch (month) {
    case 1: // we can use string like [ case "jan": ]
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("NOTA");
        break;
}